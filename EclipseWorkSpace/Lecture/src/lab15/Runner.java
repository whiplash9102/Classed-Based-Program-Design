package lab15;

public class Runner {
		String name;
		int age;
		int bib;
		boolean isMale;
		int pos;
		int time;
		
		Runner(String name,int age, int bib, boolean isMale, int pos, int time){
			this.name = name;
			this.age = age;
			this.bib = bib;
			this.isMale = isMale;
			this.pos = pos;
			this.time = time;
	}
}

